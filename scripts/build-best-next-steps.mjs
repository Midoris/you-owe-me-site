#!/usr/bin/env node

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import {
  bestNextStepStrategicUrls,
  contentRegistry,
} from "../content/content-registry.mjs";
import { renderBestNextStep, resolveBestNextSteps } from "./best-next-step-component.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const startMarker = "<!-- best-next-step:start -->";
const endMarker = "<!-- best-next-step:end -->";

function routeFilePath(url) {
  if (url === "/") return path.join(rootDir, "index.html");
  return path.join(rootDir, url.slice(1), "index.html");
}

function htmlPathFromPage(filePath, targetPath) {
  const relative = path.relative(path.dirname(filePath), targetPath).split(path.sep).join("/");
  return relative.startsWith(".") ? relative : relative;
}

function ensureStylesheet(html, filePath) {
  if (html.includes("styles/best-next-step.css")) return html;

  const stylesheetPath = path.join(rootDir, "styles", "best-next-step.css");
  const href = htmlPathFromPage(filePath, stylesheetPath);
  const link = `    <link rel="stylesheet" href="${href}" />`;

  return html.replace(/(\s*<\/head>)/, `\n${link}$1`);
}

function replaceMarkerBlock(html, moduleHtml) {
  const pattern = new RegExp(`${startMarker}[\\s\\S]*?${endMarker}`);
  const replacement = [
    startMarker,
    "<!-- Generated by scripts/build-best-next-steps.mjs. Edit content/content-registry.mjs instead. -->",
    moduleHtml,
    endMarker,
  ].join("\n");

  return html.replace(pattern, replacement);
}

function hasMarker(html) {
  return html.includes(startMarker) && html.includes(endMarker);
}

function main() {
  const byUrl = new Map(contentRegistry.map((entry) => [entry.url, entry]));
  const strategicUrls = new Set(bestNextStepStrategicUrls);
  const missingMarkers = [];
  const unresolved = [];
  let updatedCount = 0;

  for (const url of strategicUrls) {
    const entry = byUrl.get(url);
    if (!entry || entry.status !== "live") {
      unresolved.push(`${url}: strategic URL is not a live registry entry`);
      continue;
    }

    const filePath = routeFilePath(url);
    if (!fs.existsSync(filePath)) {
      unresolved.push(`${url}: route file does not exist`);
      continue;
    }

    const html = fs.readFileSync(filePath, "utf8");
    if (!hasMarker(html)) {
      missingMarkers.push(url);
      continue;
    }

    const resolved = resolveBestNextSteps(entry, byUrl);
    if (!resolved || !resolved.enabled) {
      unresolved.push(`${url}: bestNextSteps did not resolve`);
      continue;
    }

    const moduleHtml = renderBestNextStep(entry, byUrl);
    const nextHtml = ensureStylesheet(replaceMarkerBlock(html, moduleHtml), filePath);
    if (nextHtml !== html) {
      fs.writeFileSync(filePath, nextHtml);
      updatedCount += 1;
    }
  }

  if (missingMarkers.length > 0 || unresolved.length > 0) {
    if (missingMarkers.length > 0) {
      console.error("Missing best-next-step markers:");
      for (const url of missingMarkers) console.error(`- ${url}`);
    }

    if (unresolved.length > 0) {
      console.error("Unresolved best-next-step modules:");
      for (const issue of unresolved) console.error(`- ${issue}`);
    }

    process.exit(1);
  }

  console.log(`Best Next Step modules built for ${strategicUrls.size} page(s); ${updatedCount} file(s) changed.`);
}

main();
